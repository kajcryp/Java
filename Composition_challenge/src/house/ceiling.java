package house;

public class ceiling {
    private int height;
    private String paintedColour;

    public ceiling(int height, String paintedColour) {
        this.height = height;
        this.paintedColour = paintedColour;
    }

    public int getHeight() {
        return height;
    }

    public String getPaintedColour() {
        return paintedColour;
    }
}
